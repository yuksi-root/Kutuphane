    private int getKitapId() {
        int x = 0;
        try {
            Statement stmt = con.createStatement();
            ResultSet rs = stmt.executeQuery("Select max(kitapId) from Kitaplik");
            rs.next();
            x = rs.getInt(1);
        } catch (SQLException ex) {
            System.out.println("Listelerken hata oluştu");
        }
        return x;
    }

    private void btnEkleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEkleActionPerformed
        if (guncelle == true) {
            if (!kitapAd.getText().equals("") && !kitapTur.getText().equals("") && !yazar.getText().equals("")
                    && !yayinEvi.getText().equals("") && !yerNo.equals("")) {
                String sorgu = "update Kitaplik set kitapAd='" + kitapAd.getText()
                        + "' ,kitapTur='" + kitapTur.getText() + "' ,yazar='" + yazar.getText() + "',"
                        + " yayinEvi='" + yayinEvi.getText() + "', yerNo='" + yerNo.getText() + "'"
                        + " where kitapId=" + modelTable.getValueAt(jTable1.getSelectedRow(), 0);
                Guncelle g = new Guncelle(con, sorgu);
                for (int i = modelTable.getRowCount() - 1; i >= 0; i--) {
                    modelTable.removeRow(i);
                }

                modelTable = vg.getTable((DefaultTableModel) jTable1.getModel());
                kitapAd.setText("");
                kitapTur.setText("");
                yazar.setText("");
                yayinEvi.setText("");
                yerNo.setText("");
                guncelle = false;
            } else {
                JOptionPane.showMessageDialog(this, "Bütün Alanları Doldurun");
            }
        } else {
            if (!kitapAd.getText().equals("") && !kitapTur.getText().equals("") && !yazar.getText().equals("")
                    && !yayinEvi.getText().equals("") && !yerNo.equals("")) {
                String sorgu = "INSERT INTO Kitaplik (kitapId,kitapAd,kitapTur,yazar,yayinEvi,yerNo)"
                        + " VALUES ('" + (getKitapId() + 1) + "','" + kitapAd.getText() + "','" + kitapTur.getText()
                        + "','" + yazar.getText() + "','" + yayinEvi.getText() + "','" + yerNo.getText() + "')";
                Ekle e = new Ekle(con, sorgu);
                for (int i = modelTable.getRowCount() - 1; i >= 0; i--) {
                    modelTable.removeRow(i);
                }
                modelTable = vg.getTable((DefaultTableModel) jTable1.getModel());
                kitapAd.setText("");
                kitapTur.setText("");
                yazar.setText("");
                yayinEvi.setText("");
                yerNo.setText("");
            } else {
                JOptionPane.showMessageDialog(this, "Bütün Alanları Doldurun");
            }
        }

    }//GEN-LAST:event_btnEkleActionPerformed

