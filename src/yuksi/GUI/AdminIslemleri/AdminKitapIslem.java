    private void btnDuzenleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDuzenleActionPerformed
        if (jTable1.getSelectedRow() != -1) {
            guncelle = !guncelle;
            if (guncelle == true) {
                JOptionPane.showMessageDialog(this, "Guncelleme yapılıyor.");
                try {
                    Statement stmt = con.createStatement();
                    ResultSet rs = stmt.executeQuery("Select kitapAd,kitapTur,yazar,yayinEvi,yerNo from Kitaplik "
                            + " where kitapId=" + modelTable.getValueAt(jTable1.getSelectedRow(), 0));
                    rs.next();
                    kitapAd.setText(rs.getString(1));
                    kitapTur.setText(rs.getString(2));
                    yazar.setText(rs.getString(3));
                    yayinEvi.setText(rs.getString(4));
                    yerNo.setText(rs.getString(5));

                } catch (SQLException ex) {
                    System.out.println("Kitap bilgilerini alırken hata oluştu");
                }
            } else {
                kitapAd.setText("");
                kitapTur.setText("");
                yazar.setText("");
                yayinEvi.setText("");
                yerNo.setText("");
                JOptionPane.showMessageDialog(this, "Guncelleme islemi iptal edildi.");
            }

        }


    }//GEN-LAST:event_btnDuzenleActionPerformed


